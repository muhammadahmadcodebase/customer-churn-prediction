from __future__ import annotations

import csv
import json
from io import BytesIO, StringIO
from typing import Any

import pandas as pd

try:
    from fpdf import FPDF
except ImportError:
    FPDF = None


def _pdf_safe_text(value: Any) -> str:
    """Return text compatible with FPDF's built-in Latin-1 fonts."""
    text = str(value)
    replacements = {
        "\u2022": "-",
        "\u2013": "-",
        "\u2014": "-",
        "\u2018": "'",
        "\u2019": "'",
        "\u201c": '"',
        "\u201d": '"',
    }
    for old, new in replacements.items():
        text = text.replace(old, new)
    return text.encode("latin-1", errors="replace").decode("latin-1")


def _write_pdf_line(pdf: FPDF, text: Any, height: int = 6) -> None:
    pdf.set_x(pdf.l_margin)
    try:
        pdf.multi_cell(0, height, _pdf_safe_text(text), new_x="LMARGIN", new_y="NEXT")
    except TypeError:
        pdf.multi_cell(0, height, _pdf_safe_text(text))
        pdf.set_x(pdf.l_margin)


def build_pdf_report(
    customer_info: dict[str, Any],
    prediction_summary: dict[str, Any],
    recommendations: list[str],
    shap_summary: dict[str, Any],
) -> bytes | None:
    if FPDF is None:
        return None

    pdf = FPDF()
    pdf.set_auto_page_break(True, margin=15)
    pdf.add_page()

    pdf.set_font("Arial", "B", 16)
    pdf.cell(0, 10, "Customer Churn Intelligence Report", ln=True)
    pdf.set_font("Arial", "", 11)
    pdf.cell(0, 8, "Generated by the Customer Prediction dashboard.", ln=True)
    pdf.ln(5)

    pdf.set_font("Arial", "B", 12)
    pdf.cell(0, 8, "Customer Information", ln=True)
    pdf.set_font("Arial", "", 11)
    for label, value in customer_info.items():
        _write_pdf_line(pdf, f"{label}: {value}")
    pdf.ln(3)

    pdf.set_font("Arial", "B", 12)
    pdf.cell(0, 8, "Prediction Summary", ln=True)
    pdf.set_font("Arial", "", 11)
    for label, value in prediction_summary.items():
        _write_pdf_line(pdf, f"{label}: {value}")
    pdf.ln(3)

    pdf.set_font("Arial", "B", 12)
    pdf.cell(0, 8, "Recommendations", ln=True)
    pdf.set_font("Arial", "", 11)
    for recommendation in recommendations:
        pdf.set_x(pdf.l_margin)
        _write_pdf_line(pdf, f"- {recommendation}")
    pdf.ln(3)

    pdf.set_font("Arial", "B", 12)
    pdf.cell(0, 8, "SHAP Summary", ln=True)
    pdf.set_font("Arial", "", 11)
    if shap_summary:
        positive = shap_summary.get("positive", [])
        negative = shap_summary.get("negative", [])
        _write_pdf_line(pdf, "Top Positive Factors:")
        for feature, value in positive:
            _write_pdf_line(pdf, f"  - {feature}: {value:.3f}")
        _write_pdf_line(pdf, "Top Negative Factors:")
        for feature, value in negative:
            _write_pdf_line(pdf, f"  - {feature}: {value:.3f}")
    else:
        _write_pdf_line(pdf, "SHAP explanation is unavailable for this report.")

    pdf_buffer = pdf.output(dest="S")
    if isinstance(pdf_buffer, bytearray):
        return bytes(pdf_buffer)
    if isinstance(pdf_buffer, bytes):
        return pdf_buffer
    return pdf_buffer.encode("latin-1")


def build_json_report(
    customer_info: dict[str, Any],
    prediction_summary: dict[str, Any],
    recommendations: list[str],
    shap_summary: dict[str, Any],
) -> str:
    payload = {
        "customer_info": customer_info,
        "prediction_summary": prediction_summary,
        "recommendations": recommendations,
        "shap_summary": shap_summary,
    }
    return json.dumps(payload, indent=2)


def build_csv_report(
    customer_info: dict[str, Any],
    prediction_summary: dict[str, Any],
    recommendations: list[str],
    shap_summary: dict[str, Any],
) -> bytes:
    text_output = StringIO()
    writer = csv.writer(text_output)
    writer.writerow(["Section", "Attribute", "Value"])
    for label, value in customer_info.items():
        writer.writerow(["Customer Information", label, value])
    for label, value in prediction_summary.items():
        writer.writerow(["Prediction Summary", label, value])
    for recommendation in recommendations:
        writer.writerow(["Recommendations", "Recommendation", recommendation])
    for label, values in shap_summary.items():
        writer.writerow([
            "SHAP Summary",
            label,
            ", ".join([f"{feature}:{score:.3f}" for feature, score in values]),
        ])
    return text_output.getvalue().encode("utf-8")
